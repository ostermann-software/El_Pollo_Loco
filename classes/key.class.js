/**
 * Represents the current state of keyboard controls.
 */
class Keyboard {

    /**
     * Whether the left key is pressed.
     * @type {boolean}
     */
    left = false;

    /**
     * Whether the right key is pressed.
     * @type {boolean}
     */
    right = false;

    /**
     * Whether the up key is pressed.
     * @type {boolean}
     */
    up = false;

    /**
     * Whether the down key is pressed.
     * @type {boolean}
     */
    down = false;

    /**
     * Whether the space key is pressed.
     * @type {boolean}
     */
    space = false;

    /**
     * Whether the 'D' key is pressed (e.g. for debug or throw action).
     * @type {boolean}
     */
    d = false;
}
